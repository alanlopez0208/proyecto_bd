    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        int opcion = JOptionPane.showConfirmDialog(null, "¿Estas seguro de dar de Baja al Alumno " + txtNombre.getText() + " con Matricula " + txtControl.getText() + " ?");
        if (opcion == JOptionPane.YES_OPTION) {
            alumnosOp = new AlumnosOp();

            alumnosOp.eliminarAlumno(alumno.getId());
            limpiar();
        }
    }//GEN-LAST:event_btnBajaActionPerformed

