    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Falta el nombre del Alumno a registrar");
            return;
        }
        if (txtApellidos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Falta el apellido del Alumno a registrar");
            return;
        }
        if (txtTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Falta el numero de telefono del Alumno a registrar");
            return;
        }
        if (txtDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Falta la direccion del Alumno a registrar");
            return;
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(null, "¿Estas seguro de actualizar al Alumno  ?");
        if (opcion == JOptionPane.YES_OPTION) {

            if (txtNombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Falta el nombre del Alumno a registrar");
                return;
            }
            if (txtApellidos.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Falta el apellido del Alumno a registrar");
                return;
            }

            if (txtTelefono.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Falta el numero de telefono del Alumno a registrar");
                return;
            }
            if (txtDireccion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Falta la direccion del Alumno a registrar");
                return;
            }

            alumnosOp = new AlumnosOp();

            alumnosOp.actualizarAlumno(alumno.getId(), alumno);
            limpiar();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

