    private String obtenerComboBusqueda() {
        int index = comboBusquedad.getSelectedIndex();
        String busqueda = "";

        switch (index) {
            case 1:
                busqueda = "nombre";
                break;
            case 2:
                busqueda = "apellido";
                break;
            case 3:
                busqueda = "numero_control";
                break;
            default:
                break;
        }

        return busqueda;
    }

