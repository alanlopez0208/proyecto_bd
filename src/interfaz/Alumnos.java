    private void iniciarTabla() {
        alumnosOp = new AlumnosOp();
        ArrayList<Alumno> listaAlumno = alumnosOp.obtenerAlumnos();

        for (Alumno alumno : listaAlumno) {
            modeloTabla.addRow(alumno.toObject());
        }

        tablaAlumnos.setModel(modeloTabla);
    }

    public void actualizarTabla(ArrayList<Alumno> listaAlumnos) {
        modeloTabla.setNumRows(0);

        for (Alumno alumno : listaAlumnos) {
            modeloTabla.addRow(alumno.toObject());
        }

        tablaAlumnos.setModel(modeloTabla);
    }

